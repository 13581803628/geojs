//////////////////////////////////////////////////////////////////////////////
/**
 * Create a new instance of class renderer
 *
 * @param canvas
 * @returns {geo.renderer}
 */
//////////////////////////////////////////////////////////////////////////////
geo.renderer = function(container, canvas) {
  'use strict';

  if (!(this instanceof geo.renderer)) {
    return new geo.renderer(container, canvas);
  }
  geo.object.call(this);


  var m_this = this,
      m_container = container;

  ////////////////////////////////////////////////////////////////////////////
  /**
   * Get container of the renderer
   *
   * @returns {*}
   */
  ////////////////////////////////////////////////////////////////////////////
  this.container = function() {
    return m_container;
  };

  ////////////////////////////////////////////////////////////////////////////
  /**
   * Get canvas for thr renderer
   */
  ////////////////////////////////////////////////////////////////////////////
  this.canvas = function() {
    return this._canvas();
  }

  ////////////////////////////////////////////////////////////////////////////
  /**
   * Get context renderer
   */
  ////////////////////////////////////////////////////////////////////////////
  this.contextRenderer = function() {
    return this._contextRenderer();
  };

  ////////////////////////////////////////////////////////////////////////////
  /**
   * Get mouse coodinates related to canvas
   *
   * @param event
   * @returns {{x: number, y: number}}
   */
  ////////////////////////////////////////////////////////////////////////////
  this.relMouseCoords = function(event) {
    var totalOffsetX = 0,
        totalOffsetY = 0,
        canvasX = 0,
        canvasY = 0,
        currentElement = this.canvas();

    do {
      totalOffsetX += currentElement.offsetLeft - currentElement.scrollLeft;
      totalOffsetY += currentElement.offsetTop - currentElement.scrollTop;
    } while (currentElement = currentElement.offsetParent);

    canvasX = event.pageX - totalOffsetX;
    canvasY = event.pageY - totalOffsetY;

    return {
      x: canvasX,
      y: canvasY
    };
  };

  ////////////////////////////////////////////////////////////////////////////
  /**
   * Initialize
   */
  ////////////////////////////////////////////////////////////////////////////
  this._init = function() {
  };

  ////////////////////////////////////////////////////////////////////////////
  /**
   * Get/Set canvas
   */
  ////////////////////////////////////////////////////////////////////////////
  this._canvas = function(val) {
  };

  ////////////////////////////////////////////////////////////////////////////
  /**
   * Render
   */
  ////////////////////////////////////////////////////////////////////////////
  this._contextRenderer = function() {
  };

  ////////////////////////////////////////////////////////////////////////////
  /**
   * Render
   */
  ////////////////////////////////////////////////////////////////////////////
  this._render = function() {
  };

  ////////////////////////////////////////////////////////////////////////////
  /**
   * Exit
   */
  ////////////////////////////////////////////////////////////////////////////
  this._exit = function() {
  };

  this._init();
  return this;
};

inherit(geo.renderer, vgl.object);
