//////////////////////////////////////////////////////////////////////////////
/**
 * Create a new instance of class feature
 *
 * @class
 * @returns {geo.feature}
 */
//////////////////////////////////////////////////////////////////////////////
geo.feature = function() {
  "use strict";
  if (!(this instanceof geo.feature)) {
    return new geo.feature();
  }
  vgl.object.call(this);

  ////////////////////////////////////////////////////////////////////////////
  /**
   * @private
   */
  ////////////////////////////////////////////////////////////////////////////
  var m_colors = null,
      m_lookupTable = null,
      m_style = {},
      m_gcs = "EPSG:4326";

  ////////////////////////////////////////////////////////////////////////////
  /**
   * Get colors
   */
  ////////////////////////////////////////////////////////////////////////////
  this.colors = function() {
    return m_colors;
  };

  ////////////////////////////////////////////////////////////////////////////
  /**
   * Set colors
   *
   * If both colors and lookup table are set, then colors will be
   * used to color the feature. It should be noted that colors could
   * be a single value, an array, or a function.
   */
  ////////////////////////////////////////////////////////////////////////////
  this.setColors = function(colors) {
    if (colors !== m_colors) {
      m_colors = colors;
      this.modified();
      return true;
    }
    return false;
  };

  ////////////////////////////////////////////////////////////////////////////
  /**
   * Get lookup table
   */
  ////////////////////////////////////////////////////////////////////////////
  this.lookupTable = function() {
    return m_lookupTable;
  };

  ////////////////////////////////////////////////////////////////////////////
  /**
   * Set lookup table
   */
  ////////////////////////////////////////////////////////////////////////////
  this.setLookupTable = function(lut) {
    if (lut !== m_lookupTable) {
      m_lookupTable = lut;
      this.modified();
      return true;
    }
    return false;
  };

  ////////////////////////////////////////////////////////////////////////////
  /**
   * Get style
   */
  ////////////////////////////////////////////////////////////////////////////
  this.style = function() {
    return m_style;
  };

  ////////////////////////////////////////////////////////////////////////////
  /**
   * Set style of the feature
   */
  ////////////////////////////////////////////////////////////////////////////
  this.setStyle = function(style) {
    if (style !== m_style) {
      m_style = style;
      this.modified();
      return true;
    }
    return false;
  };

  ////////////////////////////////////////////////////////////////////////////
  /**
   * Get projection
   */
  ////////////////////////////////////////////////////////////////////////////
  this.gcs = function() {
    return m_gcs;
  };

  ////////////////////////////////////////////////////////////////////////////
  /**
   * Set the projection
   */
  ////////////////////////////////////////////////////////////////////////////
  this.setGcs = function(gcs) {
    if (gcs !== m_gcs) {
      m_gcs = gcs;
      this.modified();
      return true;
    }

    return false;
  };


  ////////////////////////////////////////////////////////////////////////////
  /**
   * Update
   */
  ////////////////////////////////////////////////////////////////////////////
  this.update = function() {
  };

  return this;
};

inherit(geo.feature, vgl.object);
